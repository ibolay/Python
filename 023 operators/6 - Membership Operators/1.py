# ┗━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━┛


x = ["apple", "banana"]

print("apple" not in x)   # False qayıdır, çünki "apple" x-in içində var
print("banana" in x)      # True qayıdır, çünki "banana" x-in içində var