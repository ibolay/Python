# 1 - Arithmnetic Operators 


# Arithmnetic Operators 
   # a)  +       - Toplama
   # b)  -       - Çıxma
   # c)  *       - Vurma
   # d)  /       - Bölme
   # e)  %       - Qalıqlı Bölme
   # f)  **      - Ededin Üstü
   # g)  //      - Qalıqsız Bölme


a = 6
b = 2

print( a + b  )   #  8
print( a - b  )   #  4
print( a * b  )   #  12
print( a / b  )   #  3.0
print( a % b  )   #  0
print( a ** b )   #  36
print( a // b )   #  3

