# ┃                                                            ┃
# ┃  7 - Bitwise Operators                                     ┃
# ┃                                                            ┃
# ┃  Bitwise operatorlar ədədləri binary (0 və 1) formasında   ┃
# ┃  işləyərək əməliyyat aparır. Yəni, operatorlar ədədlərin   ┃
# ┃  özünə yox, onların bitlərinə təsir edir.                  ┃
# ┃                                                            ┃
# ┃  a) &=   → AND (hər iki bit 1 olarsa 1 olur)               ┃
# ┃  b) |=   → OR (bitlərdən biri 1 olsa kifayətdir)           ┃
# ┃  c) ^=   → XOR (bitlər fərqlidirsə 1 olur)                 ┃
# ┃  d) >>=  → bitləri sağa sürüşdürür                         ┃
# ┃  e) <<=  → bitləri sola sürüşdürür                         ┃
# ┃                                                            ┃
# ┗━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━┛


x = 20
x >> 5

print(x)   # --> 20