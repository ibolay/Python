# 3 - Comparison Operators - Müqayisə Operatoru


# Comparison Operators
   # a)    ==        
   # b)    !=         
   # c)    >         
   # d)    <        
   # e)    >=          
   # f)    <=       


x = 5
y = 10

print( x == y )     # False
print( x != y )     # True
print( x > y  )     # False
print( x < y  )     # True 
print( x >= y )     # False
print( x <= y )     # True 
  
